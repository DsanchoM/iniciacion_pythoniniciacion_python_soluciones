# EJERCICIO 4: Escribe un programa que solicite al usuario una
# temperatura en grados Celsius, convierta la temperatura a 
# Fahrenheit e imprima la temperatura convertida


temperaturaGradosCelsius = int(input("Temperatura en grados Celsius: "))
temperaturaGradosFahrenheit = (((temperaturaGradosCelsius * 9)) / 5) + 32

print("Temperatura en Fahrenheit:", temperaturaGradosFahrenheit)
